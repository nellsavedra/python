x = int(input("What's x? "))
y = int(input("What's y? "))

# If operator to ask something
# Less than with operator <
if x < y:
	print("x is less than y")

# Elif operator to ask something else
# Greater than with operator >
elif x > y:
	print("x is greater than y")
	
# Else operator to catch all other cases
# Equal to with operator ==
else:
	print("x is equal to y")